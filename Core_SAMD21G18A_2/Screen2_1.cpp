void Screen2_1::doDraw(TFTMenu* menu)
{
  UTFT* dc = menu->getDC();
  dc->setColor(VGA_WHITE);
  dc->setBackColor(VGA_BLACK);
  dc->setFont(TFT_FONT);
  
  // тут рисуем, что надо именно нам, кнопки прорисуются сами после того, как мы тут всё отрисуем
  menu->print("Подключить ИУ", 15, 0);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Screen2_1::onButtonPressed(TFTMenu* menu, int pressedButton)
{
  // обработчик нажатия на кнопку. Номера кнопок начинаются с 0 и идут в том порядке, в котором мы их добавляли
  if(pressedButton == 0)
    menu->switchToScreen("SCREEN1"); // переключаемся на   экран
	else if(pressedButton == 1)
	menu->switchToScreen("SCREEN2_2"); // переключаемся на  экран
	else if (pressedButton == 2)
		menu->switchToScreen("SCREEN2_3"); // переключаемся на   экран
	else if (pressedButton == 3)
		menu->switchToScreen("SCREEN2_4"); // переключаемся на   экран
	else if (pressedButton == 4)
		menu->switchToScreen("SCREEN2_0"); // переключаемся на   экран
}
