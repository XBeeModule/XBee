InfoDiodesList InfoDiodes;
//--------------------------------------------------------------------------------------------------
InfoDiodesList::InfoDiodesList()
{
  
}
//--------------------------------------------------------------------------------------------------
void InfoDiodesList::begin()
{
  pinMode(LED_READY, OUTPUT);
  ready(false);
}
//--------------------------------------------------------------------------------------------------
void InfoDiodesList::ready(bool on)
{
  digitalWrite(LED_READY, on ? LED_ON_LEVEL : !LED_ON_LEVEL);
}
